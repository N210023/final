const mongoose = require("mongoose");

const emailverSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  otp: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
    expires: 300, // OTP expires in 5 minutes (300 seconds)
  },
});

const Emailver = mongoose.model("Emailver", emailverSchema);
module.exports = Emailver;
